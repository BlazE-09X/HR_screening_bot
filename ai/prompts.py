def build_analysis_prompt(full_name: str, qa_pairs: list[tuple[str, str, str]]) -> str:
    """qa_pairs — список (question_key, текст_вопроса, ответ_кандидата)."""
    qa_text = "\n\n".join(
        f"[{key}] Вопрос: {question}\nОтвет: {answer}" for key, question, answer in qa_pairs
    )

    return f"""Ты — ассистент рекрутера. Проанализируй анкету кандидата на основе его ответов.
ВАЖНО: ты НЕ принимаешь решение о найме, ты только помогаешь рекрутеру, давая объективный анализ.
Опирайся ТОЛЬКО на данные анкеты ниже, не придумывай факты.

Кандидат: {full_name}

{qa_text}

Проанализируй ответы и сформируй структурированную оценку: ключевые навыки (включая те, которых нет в стандартном списке — ответственность, коммуникабельность, стрессоустойчивость, обучаемость, инициативность, лидерство, командная работа, организованность, клиентоориентированность), сильные и слабые стороны, потенциальные риски (например: агрессивная манера общения, отсутствие мотивации, противоречивые ответы, уклонение от вопросов, шаблонные ответы) — как рекомендации, а не окончательный вывод, процент соответствия, общую рекомендацию, вопросы для очного собеседования, общий рейтинг 1-10, и оценку каждого отдельного ответа от 1 до 10 в поле answer_scores, используя точные ключи вопросов в квадратных скобках (например "{qa_pairs[0][0]}") как ключи словаря."""